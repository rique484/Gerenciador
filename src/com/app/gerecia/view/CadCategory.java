    private void carregaCombo() {
        DefaultComboBoxModel model = new DefaultComboBoxModel(new Categ().consultaCatogoria().toArray());
        comboCateg.setModel(model);
        modo = comboCateg.getSelectedIndex();
    }

    private void comboCategActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCategActionPerformed
        if (comboCateg.getSelectedIndex() == 0) {
            modo = 0;
            jLabel3.setText("Incluir");
            jLabel3.setForeground(Color.green);
        } else {
            modo = 1;
            jLabel3.setText("Alterar");
            jLabel3.setForeground(Color.red);
        }
    }//GEN-LAST:event_comboCategActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Categ c = new Categ();
        if (modo.equals(0)) {
            c.setCategoria(txtNome.getText());
            c.cadCateg();
            cad.carregaCombo();
            dispose();
        } else {
            c.setCategoria(txtNome.getText());
            c.setId(comboCateg.getSelectedIndex());
            c.alteraCateg();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


